#include "main.h"
#include<stdio.h>
#include<stdlib.h>
/**
 * main - Entry to the program
 * @argc: Argument counter
 * @argv: Argument vector
 *
 * Return: Always 0 (success)
 */
int main(int argc, char *argv[]__attribute__((unused)))
{
	int i;
	int x;

	for (i = 0; i < argc; i++)
	{
		x = i;
	}
	printf("%d\n", x);

	return (0);
}
